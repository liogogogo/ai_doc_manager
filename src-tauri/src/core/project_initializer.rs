fn generate_config_toml(scan: &TechScanResult) -> String {
    let has_cursorrules = scan.ai_governance.cursorrules.is_some();
    let has_windsurfrules = scan.ai_governance.windsurfrules.is_some();

    let mut rule_paths = vec!["\"AGENTS.md\"".to_string()];
    if has_cursorrules {
        rule_paths.push("\".cursorrules\"".to_string());
    }
    if has_windsurfrules {
        rule_paths.push("\".windsurfrules\"".to_string());
    }

    // Detect tech stack summary for comment
    let techs: Vec<String> = scan.languages.iter().map(|l| format!("{:?}", l)).collect();
    let fws: Vec<String> = scan.frameworks.iter().map(|f| format!("{:?}", f)).collect();
    let today = chrono::Local::now().format("%Y-%m-%d").to_string();

    let mut toml = String::new();

    // Header comment
    toml.push_str(&format!("# DocGuardian 项目配置\n"));
    toml.push_str(&format!("# 生成日期: {}\n", today));
    toml.push_str(&format!("# 技术栈: {}\n", if techs.is_empty() { "未知".to_string() } else { techs.join(", ") }));
    if !fws.is_empty() {
        toml.push_str(&format!("# 框架: {}\n", fws.join(", ")));
    }
    toml.push('\n');

    // [project]
    toml.push_str(&format!("[project]\nname = \"{}\"\n", scan.project_name));
    toml.push_str(&format!("root_path = \"{}\"\n\n", scan.root_path));

    // [layers] — 四层文档模型
    toml.push_str("# 四层文档模型\n[layers]\n");
    toml.push_str("# 宪法与边界层（AI 编码约束）\n");
    toml.push_str(&format!("rule_paths = [{}]\n", rule_paths.join(", ")));
    toml.push_str("# 状态与记忆层（AI 工作记忆，需定期 GC）\n");
    toml.push_str("state_paths = [\".ai/progress.md\"]\n");
    toml.push_str("state_capacity = 100  # 行数上限，超出自动触发 GC\n");
    toml.push_str("archive_dir = \".ai/archive\"\n");
    toml.push_str("# 契约与蓝图层（设计文档、API 规范）\n");
    if scan.existing_docs.docs_dir.is_some() {
        toml.push_str("contract_paths = [\"docs/design/*.md\"]\n");
    } else {
        toml.push_str("contract_paths = []\n");
    }
    toml.push_str("# 决策快照层（架构决策记录，只读）\n");
    if scan.existing_docs.docs_dir.is_some() {
        toml.push_str("decision_paths = [\"docs/adr/*.md\"]\n");
    } else {
        toml.push_str("decision_paths = []\n");
    }

    // [scan] — 扫描配置
    toml.push_str("\n# 扫描配置\n[scan]\n");
    toml.push_str("# 扫描时排除的目录（glob 格式）\n");
    toml.push_str("exclude_dirs = [\"node_modules\", \"target\", \"dist\", \".git\", \"__pycache__\"]\n");
    toml.push_str("# 依赖树深度解析\n");
    toml.push_str("parse_dependencies = true\n");
    toml.push_str("# CI 配置解析\n");
    toml.push_str("parse_ci_config = true\n");

    // [gc]
    toml.push_str("\n# 记忆垃圾回收配置\n[gc]\n");
    toml.push_str("enabled = true\n");
    toml.push_str("interval_minutes = 30\n");
    toml.push_str("auto_commit = false\n");
    toml.push_str("commit_message_template = \"docs(gc): archive completed items from {source}\"\n");

    // [governance] — 治理闭环
    toml.push_str("\n# 治理框架偏差检测 (F6)\n[governance]\n");
    toml.push_str("# 自动检测 AGENTS.md 与项目现状的偏差\n");
    toml.push_str("drift_detection = true\n");
    toml.push_str("# 检测维度: language, framework, tool, directory, section, ci, testing\n");
    toml.push_str("drift_dimensions = [\"language\", \"framework\", \"tool\", \"directory\", \"section\", \"ci\", \"testing\"]\n");

    // [conflict_detection]
    toml.push_str("\n# 知识冲突检测 (F3)\n[conflict_detection]\n");
    toml.push_str("enabled = false  # Phase 3 启用\n");
    if scan.git_stats.is_git_repo {
        toml.push_str("watch_branches = [\"main\"]\n");
    }
    toml.push_str("exclude_paths = [\"docs/adr/*\"]  # ADR 只读，不检测冲突\n");

    // [rule_extraction]
    toml.push_str("\n# 隐式规则提取 (F2)\n[rule_extraction]\n");
    toml.push_str("enabled = false  # Phase 2 启用\n");
    toml.push_str("failure_log = \".ai/failures.jsonl\"\n");
    toml.push_str("min_frequency = 3\n");
    toml.push_str("target_files = [\"AGENTS.md\"]\n");

    // [pruner]
    toml.push_str("\n# 冗余文档清理 (F4)\n[pruner]\n");
    toml.push_str("enabled = false  # Phase 4 启用\n");
    toml.push_str("interval_days = 7\n");
    toml.push_str("scan_extensions = [\"md\", \"txt\", \"rst\"]\n");

    // [llm] — 不再硬编码 provider，留空让用户配置
    toml.push_str("\n# LLM 配置（留空则使用全局 ~/.docguardian/llm_config.json）\n[llm]\n");
    toml.push_str("# provider = \"ollama\"  # openai | claude | zhipu | deepseek | ollama | custom\n");
    toml.push_str("# model = \"llama3.1:8b\"\n");
    toml.push_str("# base_url = \"http://localhost:11434\"\n");
    toml.push_str("# api_key = \"\"  # 建议使用环境变量而非硬编码\n");
    toml.push_str("max_tokens_per_request = 16384\n");

    toml
}
