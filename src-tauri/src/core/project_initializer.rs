/// Generate a simplified directory tree from scan results
fn dir_tree_for_scan(scan: &TechScanResult) -> Vec<String> {
    let mut lines = Vec::new();
    lines.push(format!("{}/", scan.project_name));

    // Show top-level dirs, filtered
    let skip = ["node_modules", "dist", "target", ".git", "__pycache__", ".next", "build"];
    let mut shown: Vec<&String> = scan.dir_structure.top_level_dirs
        .iter()
        .filter(|d| !skip.contains(&d.as_str()))
        .collect();
    shown.sort();

    for (i, dir) in shown.iter().enumerate() {
        let is_last = i == shown.len() - 1
            && scan.ai_governance.agents_md.is_none()
            && scan.ai_governance.progress_md.is_none();
        let prefix = if is_last { "└── " } else { "├── " };
        let annotation = dir_annotation(dir, scan);
        if annotation.is_empty() {
            lines.push(format!("{}{}/ ", prefix, dir));
        } else {
            lines.push(format!("{}{}/ {}", prefix, dir, annotation));
        }
    }

    // Key files at root
    if scan.ai_governance.agents_md.is_some() || true {
        lines.push("├── AGENTS.md".into());
    }
    lines.push("└── .docguardian.toml".into());

    lines
}

fn dir_annotation(dir: &str, scan: &TechScanResult) -> String {
    // Provide helpful annotations for common directories
    let is_tauri = scan.frameworks.iter().any(|f| matches!(f, Framework::Tauri));
    let is_react = scan.frameworks.iter().any(|f| matches!(f, Framework::React));

    match dir {
        "src" if is_react => "# React 前端源码".into(),
        "src" => "# 源码".into(),
        "src-tauri" if is_tauri => "# Tauri/Rust 后端".into(),
        "docs" => "# 文档".into(),
        "tests" | "test" | "__tests__" => "# 测试".into(),
        "public" => "# 静态资源".into(),
        "scripts" => "# 脚本".into(),
        ".github" => "# CI/CD".into(),
        ".ai" => "# AI 状态文件（progress.md）".into(),
        _ => String::new(),
    }
}

