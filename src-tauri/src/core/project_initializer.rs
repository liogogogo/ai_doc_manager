use thiserror::Error;

#[derive(Error, Debug)]
pub enum InitError {
    #[error("IO error: {0}")]
    Io(#[from] std::io::Error),
    #[error("Scan error: {0}")]
    Scan(#[from] crate::services::tech_detector::DetectorError),
    #[error("Path not found: {0}")]
    PathNotFound(String),
}

/// A single generated rule for AGENTS.md
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct GeneratedRule {
    pub id: String,
    pub category: RuleCategory,
    pub content: String,
    pub source: RuleSource,
    pub accepted: bool,
}

#[derive(Debug, Clone, Serialize, Deserialize)]
#[serde(rename_all = "snake_case")]
pub enum RuleCategory {
    TechStack,
    Directory,
    Linter,
    GitPattern,
    BestPractice,
}

#[derive(Debug, Clone, Serialize, Deserialize)]
#[serde(rename_all = "snake_case")]
pub enum RuleSource {
    LinterConfig(String),
    TechStackDetection,
    DirectoryStructure,
    GitLogAnalysis,
    FrameworkBestPractice,
}

/// A file to be generated by the initializer
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct GeneratedFile {
    pub rel_path: String,
    pub content: String,
    pub description: String,
    pub overwrite: bool,
}

/// The complete initialization plan (preview before writing)
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct InitPlan {
    pub scan_result: TechScanResult,
    pub mode: InitMode,
    pub rules: Vec<GeneratedRule>,
    pub files: Vec<GeneratedFile>,
}

#[derive(Debug, Clone, Serialize, Deserialize)]
#[serde(rename_all = "snake_case")]
pub enum InitMode {
    Fresh,
    Enhance,
}

/// Generate an initialization plan from scan results.
/// This is the deterministic layer — no LLM calls.
pub fn generate_init_plan(scan: &TechScanResult) -> InitPlan {
    let mode = if scan.ai_governance.agents_md.is_some() {
        InitMode::Enhance
    } else {
        InitMode::Fresh
    };

    let rules = generate_rules(scan);
    let files = generate_files(scan, &rules, &mode);

    InitPlan {
        scan_result: scan.clone(),
        mode,
        rules,
        files,
    }
}

/// Write accepted files to disk.
pub fn execute_init_plan(root: &Path, plan: &InitPlan) -> Result<Vec<String>, InitError> {
    if !root.exists() {
        return Err(InitError::PathNotFound(root.display().to_string()));
    }

    let mut written = Vec::new();

    for file in &plan.files {
        let target = root.join(&file.rel_path);

        // Skip if file exists and overwrite is false
        if target.exists() && !file.overwrite {
            continue;
        }

        // Create parent directories
        if let Some(parent) = target.parent() {
            std::fs::create_dir_all(parent)?;
        }

        std::fs::write(&target, &file.content)?;
        written.push(file.rel_path.clone());
        tracing::info!("Generated: {}", file.rel_path);
    }

    Ok(written)
}

// --- Rule generation engine ---

fn generate_rules(scan: &TechScanResult) -> Vec<GeneratedRule> {
    let mut rules = Vec::new();
    let mut id_counter = 0u32;

    let mut next_id = || -> String {
        id_counter += 1;
        format!("rule_{:03}", id_counter)
    };

    // 1. Rules from linter configs (skip items already enforced by tools)
    for tool in &scan.tools {
        let tool_rules = rules_from_tool(tool);
        for (content, skip) in tool_rules {
            if skip {
                continue; // Already enforced by tool, no need to document
            }
            rules.push(GeneratedRule {
                id: next_id(),
                category: RuleCategory::Linter,
                content,
                source: RuleSource::LinterConfig(tool.config_file.clone()),
                accepted: true,
            });
        }
    }

    // 2. Rules from tech stack
    for lang in &scan.languages {
        for rule in rules_for_language(lang) {
            rules.push(GeneratedRule {
                id: next_id(),
                category: RuleCategory::TechStack,
                content: rule,
                source: RuleSource::TechStackDetection,
                accepted: true,
            });
        }
    }

    for fw in &scan.frameworks {
        for rule in rules_for_framework(fw) {
            rules.push(GeneratedRule {
                id: next_id(),
                category: RuleCategory::TechStack,
                content: rule,
                source: RuleSource::FrameworkBestPractice,
                accepted: true,
            });
        }
    }

    // 3. Rules from directory structure
    for rule in rules_from_dir_structure(&scan.dir_structure) {
        rules.push(GeneratedRule {
            id: next_id(),
            category: RuleCategory::Directory,
            content: rule,
            source: RuleSource::DirectoryStructure,
            accepted: true,
        });
    }

    // 4. Rules from git log patterns
    if scan.git_stats.fix_commits >= 3 {
        rules.push(GeneratedRule {
            id: next_id(),
            category: RuleCategory::GitPattern,
            content: format!(
                "项目近期有 {} 个 fix commit，请特别注意代码质量和边界情况处理。",
                scan.git_stats.fix_commits
            ),
            source: RuleSource::GitLogAnalysis,
            accepted: true,
        });
    }

    for pattern in &scan.git_stats.recent_fix_patterns {
        let lower = pattern.to_lowercase();
        if lower.contains("timeout") || lower.contains("超时") {
            rules.push(GeneratedRule {
                id: next_id(),
                category: RuleCategory::GitPattern,
                content: "所有外部调用（HTTP、数据库、RPC）必须设置超时时间。".into(),
                source: RuleSource::GitLogAnalysis,
                accepted: true,
            });
            break;
        }
    }

    for pattern in &scan.git_stats.recent_fix_patterns {
        let lower = pattern.to_lowercase();
        if lower.contains("null") || lower.contains("undefined") || lower.contains("unwrap") {
            rules.push(GeneratedRule {
                id: next_id(),
                category: RuleCategory::GitPattern,
                content: "注意空值/None/undefined 的处理，禁止在生产路径中使用 unwrap() 或未检查的 nullable 访问。".into(),
                source: RuleSource::GitLogAnalysis,
                accepted: true,
            });
            break;
        }
    }

    rules
}

