fn code_examples_for_scan(scan: &TechScanResult) -> Vec<String> {
    let mut examples = Vec::new();

    // Rust error handling
    if scan.languages.iter().any(|l| matches!(l, Language::Rust)) {
        examples.push(
            "**Rust 错误处理**\n\
             ```rust\n\
             // ✅ Good\n\
             fn read_config(path: &Path) -> Result<Config, AppError> {\n\
             \x20   let content = std::fs::read_to_string(path)\n\
             \x20       .map_err(|e| AppError::Io(e))?;\n\
             \x20   Ok(toml::from_str(&content)?)\n\
             }\n\
             \n\
             // ❌ Bad\n\
             fn read_config(path: &Path) -> Config {\n\
             \x20   let content = std::fs::read_to_string(path).unwrap();\n\
             \x20   toml::from_str(&content).unwrap()\n\
             }\n\
             ```".into()
        );
    }

    // TypeScript
    if scan.languages.iter().any(|l| matches!(l, Language::TypeScript)) {
        examples.push(
            "**TypeScript 类型安全**\n\
             ```typescript\n\
             // ✅ Good\n\
             const fetchUser = async (id: string): Promise<User> => {\n\
             \x20 const res = await api.get<User>(`/users/${id}`);\n\
             \x20 return res.data;\n\
             };\n\
             \n\
             // ❌ Bad\n\
             const fetchUser = async (id: any) => {\n\
             \x20 const res = await api.get('/users/' + id) as any;\n\
             \x20 return res.data;\n\
             };\n\
             ```".into()
        );
    }

    // Tauri IPC
    if scan.frameworks.iter().any(|f| matches!(f, Framework::Tauri)) {
        examples.push(
            "**Tauri IPC 调用**\n\
             ```typescript\n\
             // ✅ Good — 使用封装的 hook\n\
             const cmd = useTauriCommand<Project>(\"add_project\");\n\
             const result = await cmd.execute({ root_path: path });\n\
             \n\
             // ❌ Bad — 裸调 invoke\n\
             const result = await invoke(\"add_project\", { rootPath: path });\n\
             ```".into()
        );
    }

    // React
    if scan.frameworks.iter().any(|f| matches!(f, Framework::React)) {
        examples.push(
            "**React 组件**\n\
             ```tsx\n\
             // ✅ Good — 函数组件 + TypeScript props\n\
             interface Props { title: string; onClose: () => void }\n\
             function Modal({ title, onClose }: Props) {\n\
             \x20 return <div>...</div>;\n\
             }\n\
             \n\
             // ❌ Bad — class component + any\n\
             class Modal extends React.Component<any> { ... }\n\
             ```".into()
        );
    }

    // 其他语言的代码示例交给 LLM 智能层生成（更贴合项目上下文）

    examples
}

/// Seed common mistakes based on known tech-stack pitfalls
fn common_mistakes_for_scan(scan: &TechScanResult) -> Vec<String> {
    let mut mistakes = Vec::new();

    // Tauri-specific
    if scan.frameworks.iter().any(|f| matches!(f, Framework::Tauri)) {
        mistakes.push(
            "Tauri IPC 参数命名不一致：前端用 `snake_case`，后端 `#[tauri::command]` 默认期望 `camelCase`。\
             务必添加 `rename_all = \"snake_case\"`。".into()
        );
    }

    // Rust
    if scan.languages.iter().any(|l| matches!(l, Language::Rust)) {
        mistakes.push(
            "在 `async fn` 中使用 `std::fs` 同步 IO 会阻塞 tokio runtime。\
             对于重 IO 操作应使用 `tokio::fs` 或 `spawn_blocking`。".into()
        );
    }

    // React
    if scan.frameworks.iter().any(|f| matches!(f, Framework::React)) {
        mistakes.push(
            "`useEffect` 中缺少 cleanup 函数导致内存泄漏。订阅、定时器、异步操作都需要 cleanup。".into()
        );
    }

    // TypeScript
    if scan.languages.iter().any(|l| matches!(l, Language::TypeScript)) {
        mistakes.push(
            "导入路径使用相对路径 `../../` 过深。应使用 `@/` 路径别名保持导入清晰。".into()
        );
    }

    // Python
    if scan.languages.iter().any(|l| matches!(l, Language::Python)) {
        mistakes.push(
            "在函数参数默认值中使用可变对象（如 `def f(x=[])`），会导致所有调用共享同一实例。".into()
        );
    }

    // 其他语言的常犯错误交给 LLM 智能层生成（更贴合项目上下文）

    mistakes
}

